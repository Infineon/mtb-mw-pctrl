/** Logging levels. NOTE: Default value for all facilities is passed in to init call */
typedef enum
{
    CY_PCTRL_LOG_OFF = 0,       /**< Do not print log messages */
    CY_PCTRL_LOG_ERROR,         /**< Print if run-time level is <= CY_PCTRL_LOG_ERROR   */
    CY_PCTRL_LOG_WARNING,       /**< Print if run-time level is <= CY_PCTRL_LOG_WARNING */
    CY_PCTRL_LOG_NOTICE,        /**< Print if run-time level is <= CY_PCTRL_LOG_NOTICE  */
    CY_PCTRL_LOG_INFO,          /**< Print if run-time level is <= CY_PCTRL_LOG_INFO    */
    CY_PCTRL_LOG_DEBUG,         /**< Print if run-time level is <= CY_PCTRL_LOG_DEBUG   */
    CY_PCTRL_LOG_DEBUG1,        /**< Print if run-time level is <= CY_PCTRL_LOG_DEBUG1  */
    CY_PCTRL_LOG_DEBUG2,        /**< Print if run-time level is <= CY_PCTRL_LOG_DEBUG2  */
    CY_PCTRL_LOG_DEBUG3,        /**< Print if run-time level is <= CY_PCTRL_LOG_DEBUG3  */
    CY_PCTRL_LOG_DEBUG4,        /**< Print if run-time level is <= CY_PCTRL_LOG_DEBUG4  */

    CY_PCTRL_LOG_PRINT,         /* Identifies the log messages generated by cy_log_printf calls */

    CY_PCTRL_LOG_MAX
} CY_PCTRL_LOG_LEVEL_T;

/** Log facility type
 * Log facilities allow for separate subsystems to have different run-time log levels for output.
 * This allows for someone working in the Driver subsystem to turn on DEBUG level without turning on
 * DEBUG level for middleware - Makes for less unwanted output during debugging/testing.
 */
typedef enum
{
    CYLF_PCTRL_DEFAULT = 0,     /**< General log message not associated with any specific facility */
    CYLF_PCTRL_MIDDLEWARE,      /**< Middleware facility */

    CYLF_PCTRL_MAX              /**< Must be last, not an actual index */
} CY_PCTRL_LOG_FACILITY_T;

