#define cy_pctrl_log_msg(facility, level, fmt, ...)
#define cy_pctrl_assert_msg(assertion, facility, level, fmt, ...)
#endif

#define cy_pctrl_assert_msg_(assertion, facility, level, fmt, ...)    \
    do { if (!(assertion) ) { cy_pctrl_log_msg_(facility, level, fmt, __VA_ARGS__); CY_ASSERT_HANDLER(); } } while (0)

