/**
 * @file
 * @addtogroup pctrl_logging_utils
 *
 * A logging subsystem that allows run time control for the logging level.
 * Log messages are passed back to the application for output.
 * Log messages are given sequence numbers.
 * A time callback can be provided by the application for the timestamp for each output line.
 * Log messages are mutex protected across threads so that log messages do not interrupt each other.
 */
/*
 * In the main application file:
 *
 *  Log output callback function - The application decides what and how logging is to be output
 *
 *  int app_log_output_callback(CY_PCTRL_LOG_FACILITY_T facility, CY_PCTRL_LOG_LEVEL_T level, char
 * *logmsg)
 *  {
 *      (void)facility;     // Can be used to decide to reduce the output or send output to remote
 * logging
 *      (void)level;        // Can be used to decide to reduce the output, although the output has
 * already been
 *                          // limited by the log routines
 *
 *      return printf( "%s\n", logmsg);   // print directly to console
 *  }
 *
 *
 *  Log time callback - get the current time for the log message timestamp in ms
 *
 *  cy_rslt_t app_log_time(uint32_t* time)
 *  {
 *      if (time != NULL)
 *      {
 *          *time = get_time_ms(); // get system time (in ms)
 *      }
 *      return CY_RSLT_SUCCESS;
 *  }
 *
 *
 *  Log initialization - default or OFF, no output from any facility
 *
 *  result = cy_pctrl_log_init(CY_PCTRL_LOG_OFF, app_log_output_callback, app_log_time);
 *  if (result != CY_RSLT_SUCCESS)
 *  {
 *      printf("cy_pctrl_log_init() FAILED %ld\n", result);
 *  }
 *
 *
 *  Example using the TEST facility
 *
 *  // Sets the log message level to WARNING
 *  cy_pctrl_log_set_facility_level(CYLF_PCTRL_TEST, CY_PCTRL_LOG_WARNING);
 *
 *  // Bypasses the facility/level check and always print message
 *  // Calls app_log_output_callback(CYLF_PCTRL_DEF, CY_PCTRL_LOG_PRINT, logmsg)
 *  cy_pctrl_log_printf("MIDDLEWARE message: always print.");
 *
 *  // Print if PCTRL_CYLF_TEST level is CY_PCTRL_LOG_WARNING or higher
 *  cy_pctrl_log_msg(CYLF_PCTRL_MIDDLEWARE, CY_PCTRL_LOG_ERROR, "MIDDLEWARE message: ERROR");
 *  // Print if PCTRL_CYLF_TEST level is CY_PCTRL_LOG_WARNING or higher
 *  cy_pctrl_log_msg(CYLF_PCTRL_MIDDLEWARE, CY_PCTRL_LOG_WARNING, "MIDDLEWARE message: WARNING");
 *
 *  Output:
 *
 *  MIDDLEWARE message: always print.
 *  MIDDLEWARE message: ERROR
 *  MIDDLEWARE message: WARNING
 */

